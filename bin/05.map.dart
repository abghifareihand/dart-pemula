void main() {
  Map siswa = {'nama': 'Agoy', 'umur': '19', 'agama': 'Islam'};
  // isi map adalah => {'key': 'value'}
  // dan pakai kurung kurawal -> {}

  print(siswa);

  // menampilkan value dengan key 'nama'
  print(siswa['nama']);

  // menampilkan keys yaitu nama, umur, agama
  print(siswa.keys);

  // menampilkan values yaitu Agoy, 19, Islam
  print(siswa.values);
}
